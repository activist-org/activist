<template>
  <div
    v-if="windowWidth < BreakpointMap.SMALL"
    class="flex flex-col items-center justify-between gap-8 bg-layer-0 px-8 py-8 text-primary-text"
  >
    <Head>
      <Title>{{ organization.name }} </Title>
    </Head>
    <div class="mx-auto h-[260px] w-3/4">
      <ImageOrganization
        :imgUrl="organization?.iconUrl"
        :alt="
          $t('_global.entity_logo', {
            entity_name: organization?.name,
          })
        "
      />
    </div>
    <div class="flex flex-col items-center gap-2">
      <h1 class="responsive-h1 text-3xl font-bold text-primary-text">
        {{ organization.name }}
      </h1>
      <h2
        class="responsive-h2 text-center text-lg font-bold text-distinct-text"
      >
        {{ organization.tagline }}
      </h2>
    </div>
    <div class="flex w-full flex-col items-center gap-4">
      <MenuLinkWrapper
        v-for="button in organizationButtons"
        :key="button.id"
        :to="localePath(button.routeUrl)"
        :selected="button.selected"
      >
        <div
          class="flex w-full items-center space-x-2 text-left text-sm font-medium"
        >
          <span class="width-1/6"
            ><Icon
              v-if="button.iconUrl"
              :name="button.iconUrl"
              class="h-5 w-5 flex-shrink-0"
          /></span>
          <p
            class="width-5/6 hover:menu-selection select-none whitespace-nowrap text-lg font-bold"
          >
            <span class="sr-only">{{ $t("_global.navigate_to") }}</span>
            {{ $t(button.label) }}
          </p>
        </div>
      </MenuLinkWrapper>
      <BtnRouteInternal
        class="w-max"
        :cta="true"
        linkTo="/"
        label="_global.offer_to_help"
        fontSize="base"
        :rightIcon="IconMap.ARROW_RIGHT"
        iconSize="1.25em"
        ariaLabel="_global.offer_to_help_aria_label"
      />
    </div>
  </div>
</template>

<script setup lang="ts">
import useMenuEntriesState from "~/composables/useMenuEntriesState";
import { BreakpointMap } from "~/types/breakpoint-map";
import { IconMap } from "~/types/icon-map";
import type { MenuSelector } from "~/types/menu/menu-selector";

const idParam = useRoute().params.id;
const id = typeof idParam === "string" ? idParam : undefined;

const organizationStore = useOrganizationStore();
await organizationStore.fetchById(id);

const { organization } = organizationStore;

const localePath = useLocalePath();

const { organizationEntry } = useMenuEntriesState();

const organizationButtons: MenuSelector[] = [];

organizationEntry.value.forEach((entry) => {
  organizationButtons.push(entry);
});

const windowWidth = ref(window.innerWidth);

const handleResize = () => {
  windowWidth.value = window.innerWidth;
  if (windowWidth.value > BreakpointMap.SMALL) {
    const { locale } = useI18n();
    const currentRoute = useRoute();

    if (
      currentRoute.path !== `/${locale.value}/organizations/${id}/about` ||
      currentRoute.path === `/${locale.value}/organizations/${id}/`
    ) {
      navigateTo(`/${locale.value}/organizations/${id}/about`);
    }
  }
};

onMounted(() => {
  handleResize();
  window.addEventListener("resize", handleResize);
});

onUpdated(() => {
  handleResize();
});

onUnmounted(() => {
  window.removeEventListener("resize", handleResize);
});
</script>
